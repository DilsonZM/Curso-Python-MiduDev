#Conversioens de tipo en python
#Tranformar un tipo de dato en otro

print("Conversiones de tipo en Python")

print(str(2) + "3")  # Convertir un entero a cadena
print(int("123") + 1)  # Convertir una cadena a entero
print(float("3.14") + 1)  # Convertir una cadena a flotante
print(bool(0))  # Convertir un entero a booleano
print(bool(""))  # Convertir una cadena vacía a booleano
print(list("abc"))  # Convertir una cadena a lista de caracteres
print(tuple([1, 2, 3]))  # Convertir una lista a tupla
print(set([1, 2, 2, 3]))  # Convertir una lista a conjunto
print(dict([("a", 1), ("b", 2)]))  # Convertir una lista de tuplas a diccionario
print(range(5))  # Crear un rango de números
print(type(None))  # Tipo NoneType 
print(complex(1, 2))  # Crear un número complejo
print(bytes("hello", "utf-8"))  # Convertir una cadena a bytes

