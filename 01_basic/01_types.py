##Tipos de datos en Python

##int double str float bool none complex list tuple set dict range NoneType

print("int:", type(1))
print("double:", type(1.0))
print("str:", type("Hola"))
print("float:", type(1.0))
print("bool:", type(True))
print("none:", type(None))
print("complex:", type(1 + 2j))
print("list:", type([1, 2, 3]))
print("tuple:", type((1, 2, 3)))
print("set:", type({1, 2, 3}))
print("dict:", type({"a": 1, "b": 2}))
print("range:", type(range(5)))
print("NoneType:", type(type(None)))

