#Variables en python

#Sirven para almacenar datos y pueden cambiar su valor
print("Variables en Python")
x = 10  # Variable entera
y = 3.14  # Variable flotante
z = "Hola"  # Variable de cadena
print("x:", x, "y:", y, "z:", z)

print("con f-strings")
# Usando f-strings para formatear la salida
print(f"x: {x}, y: {y}, z: {z}")

# Forma no recomendada de declarar variables

x, y, z = 1, 2, 3  # Asignación múltiple
print("Asignación múltiple")
print("x:", x, "y:", y, "z:", z)


