# Ejemplo de uso de if, elif y else

print("Sentencia simple condicional")

edad = input("¿Cuál es tu edad?:")
edad = int(edad)

#saber si es mayor de edad
if edad >= 18:
    print("Eres mayor de edad")
else:
    print("Eres menor de edad")
    
